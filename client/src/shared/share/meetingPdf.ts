export async function generateMeetingPdfFile(meeting: Meeting) {
  const [{ default: html2canvas }, { jsPDF }] = await Promise.all([
    import('html2canvas'),
    import('jspdf'),
  ]);
  const container = createPdfContainer(meeting);
  document.body.appendChild(container);

  try {
    await document.fonts?.ready;
    await new Promise((resolve) => requestAnimationFrame(resolve));

    const canvas = await html2canvas(container, {
      backgroundColor: '#ffffff',
      scale: Math.min(window.devicePixelRatio || 2, 2),
      useCORS: true,
      windowWidth: 794,
    });

    const pdf = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
    const pageWidth = 210;
    const pageHeight = 297;
    const margin = 10;
    const imageWidth = pageWidth - margin * 2;
    const imageHeight = (canvas.height * imageWidth) / canvas.width;
    const imageData = canvas.toDataURL('image/png');

    let heightLeft = imageHeight;
    let y = margin;

    pdf.addImage(imageData, 'PNG', margin, y, imageWidth, imageHeight);
    heightLeft -= pageHeight - margin * 2;

    while (heightLeft > 0) {
      pdf.addPage();
      y = margin - (imageHeight - heightLeft);
      pdf.addImage(imageData, 'PNG', margin, y, imageWidth, imageHeight);
      heightLeft -= pageHeight - margin * 2;
    }

    const blob = pdf.output('blob');
    const fileName = `${safeFileName(meeting.title || 'meeting')}.pdf`;
    return new File([blob], fileName, { type: 'application/pdf' });
  } finally {
    document.body.removeChild(container);
  }
}
