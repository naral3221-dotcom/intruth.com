import type { Meeting } from '@/types';

function escapeHtml(value?: string | number | null) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function richTextHtml(value?: string) {
  if (!value) return '<p>작성된 회의 내용이 없습니다.</p>';
  if (/<[a-z][\s\S]*>/i.test(value)) return value;
  return `<p>${escapeHtml(value).replace(/\n/g, '<br />')}</p>`;
}

function formatDate(value: string) {
  return new Date(value).toLocaleString('ko-KR', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    weekday: 'short',
    hour: '2-digit',
    minute: '2-digit',
  });
}

function safeFileName(value: string) {
  return value
    .replace(/[\\/:*?"<>|]/g, '')
    .replace(/\s+/g, '_')
    .slice(0, 80);
}

function renderInfo(label: string, value?: string | number | null) {
  if (!value) return '';
  return `
    <div class="info-item">
      <dt>${escapeHtml(label)}</dt>
      <dd>${escapeHtml(value)}</dd>
    </div>
  `;
}

function renderList(title: string, items?: string[]) {
  const filteredItems = items?.filter(Boolean) || [];
  if (filteredItems.length === 0) return '';

  return `
    <section>
      <h2>${escapeHtml(title)}</h2>
      <ul>
        ${filteredItems.map((item) => `<li>${escapeHtml(item)}</li>`).join('')}
      </ul>
    </section>
  `;
}

function buildMeetingPdfHtml(meeting: Meeting) {
  const attendeeNames = meeting.attendees?.map((attendee) => attendee.member?.name).filter(Boolean) || [];
  const agendaItems = meeting.agendas?.map((agenda) => {
    return `${agenda.title}${agenda.description ? ` - ${agenda.description}` : ''}`;
  });
  const actionItems = meeting.actionItems?.map((item) => {
    const assignee = item.assignee?.name ? ` / 담당: ${item.assignee.name}` : '';
    const due = item.dueDate ? ` / 마감: ${new Date(item.dueDate).toLocaleDateString('ko-KR')}` : '';
    return `${item.title}${assignee}${due}`;
  });

  return `
    <article class="meeting-pdf">
      <header>
        <p class="eyebrow">INTRUTH 회의자료</p>
        <h1>${escapeHtml(meeting.title)}</h1>
        <dl>
          ${renderInfo('일시', formatDate(meeting.meetingDate))}
          ${renderInfo('팀', meeting.team?.name)}
          ${renderInfo('프로젝트', meeting.project?.name)}
          ${renderInfo('작성자', meeting.author?.name)}
        </dl>
      </header>

      ${renderList('참석자', attendeeNames)}

      <section>
        <h2>회의 내용</h2>
        <div class="content">${richTextHtml(meeting.content)}</div>
      </section>

      ${
        meeting.summary
          ? `<section><h2>결정사항 / 요약</h2><div class="summary">${richTextHtml(meeting.summary)}</div></section>`
          : ''
      }

      ${renderList('안건', agendaItems)}
      ${renderList('액션 아이템', actionItems)}

      <footer>
        <span>Generated by INTRUTH</span>
        <span>${new Date().toLocaleDateString('ko-KR')}</span>
      </footer>
    </article>
  `;
}

